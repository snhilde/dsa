// ASCIICharSet creates a new character set with all 128 ASCII characters.
func ASCIICharSet() CharSet {
	c := make([]rune, 128)
	for i := range c {
		c[i] = rune(i)
	}

	charSet, _ := NewCharSet(c)

	return charSet
}

